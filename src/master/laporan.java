    private void tampilDataHasilAsosiasi() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Proses");
        model.addColumn("Tanggal Proses");
        model.addColumn("Mulai Tanggal");
        model.addColumn("Sampai Tanggal");
        model.addColumn("Min Support (%)");
        model.addColumn("Min Confidence (%)");
        try {
            Connection conn = koneksi.getConnection();

            String sql = "SELECT * FROM hasil_asosiasi ORDER BY id DESC";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id"),
                    rs.getDate("tanggal_proses"),
                    rs.getDate("mulai_tanggal"),
                    rs.getDate("sampai_tanggal"),
                    rs.getDouble("min_support"),
                    rs.getDouble("min_confidence")
                });
            }
            tableProses.setModel(model);
            rs.close();
            pst.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
