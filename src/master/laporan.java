    private void bHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bHapusActionPerformed
        String id = tIDProses.getText().trim();
            if (id.isEmpty()) {
                JOptionPane.showMessageDialog(this,
                        "Pilih terlebih dahulu ID Proses yang akan dihapus.");
                return;
            }
            int konfirmasi = JOptionPane.showConfirmDialog(
                    this,
                    "Yakin ingin menghapus data dengan ID " + id + " ?",
                    "Konfirmasi",
                    JOptionPane.YES_NO_OPTION);
            if (konfirmasi == JOptionPane.YES_OPTION) {
                try {
                    Connection conn = koneksi.getConnection();

                    String sql = "DELETE FROM hasil_asosiasi WHERE id=?";
                    PreparedStatement pst = conn.prepareStatement(sql);
                    pst.setString(1, id);

                    int hasil = pst.executeUpdate();
                    if (hasil > 0) {
                        JOptionPane.showMessageDialog(this,
                                "Data berhasil dihapus.");
                        tIDProses.setText("");
                        tampilDataHasilAsosiasi();
                    } else {
                        JOptionPane.showMessageDialog(this,
                                "Data tidak ditemukan.");
                    }
                    pst.close();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e.getMessage());
                }
            }
    }//GEN-LAST:event_bHapusActionPerformed

    private void bDataLayananActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bDataLayananActionPerformed
        try {

            Connection conn = koneksi.getConnection();

            HashMap<String, Object> parameter = new HashMap<>();

            parameter.put("NAMA_ADMIN", session.namaLengkap);
            
            parameter.put(
                "LOGO",
                getClass().getResourceAsStream("/gambar/logo.png")
            );
            

            InputStream report = getClass().getResourceAsStream("/laporan/dataLayanan.jasper");

            JasperPrint jp = JasperFillManager.fillReport(report, parameter, conn);

            JasperViewer.viewReport(jp, false);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_bDataLayananActionPerformed

