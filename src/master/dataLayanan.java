    private void idOtomatis() {
        try {
            Connection conn = koneksi.getConnection();

            String sql = "SELECT id FROM datalayanan ORDER BY id DESC LIMIT 1";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                String id = rs.getString("id").substring(1);
                int no = Integer.parseInt(id) + 1;

                tIDLayanan.setText(String.format("L%03d", no));
            } else {
                tIDLayanan.setText("L001");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    public void tampilData() {
        DefaultTableModel model = new DefaultTableModel();
        NumberFormat rupiah = NumberFormat.getCurrencyInstance(new Locale("id", "ID"));
        
        model.addColumn("ID");
        model.addColumn("Nama Layanan");
        model.addColumn("Harga");

        try {
            Connection conn = koneksi.getConnection();
            String sql = "SELECT * FROM datalayanan ORDER BY id";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id"),
                    rs.getString("nama_layanan"),
                    rupiah.format(rs.getLong("harga")).replace(",00","")
                });
            }

            tableLayanan.setModel(model);

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

