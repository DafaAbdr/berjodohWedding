    private void bUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bUbahActionPerformed
        if (tIDLayanan.getText().isEmpty() ||
        tNamaLayanan.getText().isEmpty() ||
        tHarga.getText().isEmpty()) {

            javax.swing.JOptionPane.showMessageDialog(this, "Lengkapi data terlebih dahulu!");
            return;
        }

        int konfirmasi = javax.swing.JOptionPane.showConfirmDialog(
                this,
                "Apakah Anda yakin ingin mengubah data ini?",
                "Konfirmasi Ubah",
                javax.swing.JOptionPane.YES_NO_OPTION);

        if (konfirmasi == javax.swing.JOptionPane.YES_OPTION) {
            try {
                Connection conn = koneksi.getConnection();

                String sql = "UPDATE datalayanan SET nama_layanan=?, harga=? WHERE id=?";
                PreparedStatement pst = conn.prepareStatement(sql);

                pst.setString(1, tNamaLayanan.getText());
                pst.setLong(2, Long.parseLong(tHarga.getText()));
                pst.setString(3, tIDLayanan.getText());

                pst.executeUpdate();

                javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil diubah.");

                tampilData();
                bersih();

            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Gagal mengubah data!\n" + e.getMessage());
            }
        }
    }//GEN-LAST:event_bUbahActionPerformed

    private void bHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bHapusActionPerformed
        if (tIDLayanan.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan dihapus!");
            return;
        }

        int konfirmasi = JOptionPane.showConfirmDialog(
                this,
                "Apakah Anda yakin ingin menghapus data ini?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION);

        if (konfirmasi == JOptionPane.YES_OPTION) {
            try {
                Connection conn = koneksi.getConnection();

                String sql = "DELETE FROM datalayanan WHERE id = ?";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, tIDLayanan.getText());

                pst.executeUpdate();

                JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");

                tampilData();
                bersih();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal menghapus data!\n" + e.getMessage());
            }
        }
    }//GEN-LAST:event_bHapusActionPerformed

    private void tableLayananMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableLayananMouseClicked
        int baris = tableLayanan.getSelectedRow();

        tIDLayanan.setText(tableLayanan.getValueAt(baris, 0).toString());
        tNamaLayanan.setText(tableLayanan.getValueAt(baris, 1).toString());

        String harga = tableLayanan.getValueAt(baris, 2).toString();
        harga = harga.replace("Rp.", "")
                     .replace("Rp", "")
                     .replace(".", "")
                     .replace(",", "")
                     .trim();

        tHarga.setText(harga);
    }//GEN-LAST:event_tableLayananMouseClicked

