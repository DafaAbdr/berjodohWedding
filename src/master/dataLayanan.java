    private void bTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTambahActionPerformed
        if (tIDLayanan.getText().isEmpty() ||
        tNamaLayanan.getText().isEmpty() ||
        tHarga.getText().isEmpty()) {

        javax.swing.JOptionPane.showMessageDialog(this, "Lengkapi semua data terlebih dahulu!");
        return;
    }

    try {
        Connection conn = koneksi.getConnection();

        String sql = "INSERT INTO datalayanan (id, nama_layanan, harga) VALUES (?, ?, ?)";
        PreparedStatement pst = conn.prepareStatement(sql);

        pst.setString(1, tIDLayanan.getText());
        pst.setString(2, tNamaLayanan.getText());
        pst.setLong(3, Long.parseLong(tHarga.getText()));

        pst.executeUpdate();

        javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan.");

        tampilData();
        bersih();
        idOtomatis();
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal menambahkan data!\n" + e.getMessage());
    }
    }//GEN-LAST:event_bTambahActionPerformed


